package com.Pojo;

public class Salary {
	int basicPay;
	float hra;
	float da;
	float ca;
	float ma;
	float pt;
	float pf;
	float tds;
	float gs;
	public int getBasicPay() {
		return basicPay;
	}
	public void setBasicPay(int basicPay) {
		this.basicPay = basicPay;
	}
	public float getHra() {
		return hra;
	}
	public void setHra(float hra) {
		this.hra = hra;
	}
	public float getDa() {
		return da;
	}
	public void setDa(float da) {
		this.da = da;
	}
	public float getCa() {
		return ca;
	}
	public void setCa(float ca) {
		this.ca = ca;
	}
	public float getMa() {
		return ma;
	}
	public void setMa(float ma) {
		this.ma = ma;
	}
	public float getPt() {
		return pt;
	}
	public void setPt(float pt) {
		this.pt = pt;
	}
	public float getPf() {
		return pf;
	}
	public void setPf(float pf) {
		this.pf = pf;
	}
	public float getTds() {
		return tds;
	}
	public void setTds(float tds) {
		this.tds = tds;
	}
	public float getGs() {
		return gs;
	}
	public void setGs(float gs) {
		this.gs = gs;
	}
}
